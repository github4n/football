package com.visolink.entity.dto;

public class OtherLeagueOddsDTO {
	
	private String winOdds;
	
	private String drawOdds;
	
	private String loseOdds;
	
	private Long gameId;
	
	private Integer companyId;
	
	private Integer type;
	
	private Double handicap;
	
	private String handicapName;

	public String getWinOdds() {
		return winOdds;
	}

	public void setWinOdds(String winOdds) {
		this.winOdds = winOdds;
	}

	public String getDrawOdds() {
		return drawOdds;
	}

	public void setDrawOdds(String drawOdds) {
		this.drawOdds = drawOdds;
	}

	public String getLoseOdds() {
		return loseOdds;
	}

	public void setLoseOdds(String loseOdds) {
		this.loseOdds = loseOdds;
	}

	public Long getGameId() {
		return gameId;
	}

	public void setGameId(Long gameId) {
		this.gameId = gameId;
	}

	public Integer getCompanyId() {
		return companyId;
	}

	public void setCompanyId(Integer companyId) {
		this.companyId = companyId;
	}

	public Integer getType() {
		return type;
	}

	public void setType(Integer type) {
		this.type = type;
	}

	public Double getHandicap() {
		return handicap;
	}

	public void setHandicap(Double handicap) {
		this.handicap = handicap;
	}

	public String getHandicapName() {
		return handicapName;
	}

	public void setHandicapName(String handicapName) {
		this.handicapName = handicapName;
	}

}
